WHERE BOL_ID IN (SELECT BOL_ID FROM TEMP_PES.DBO.DLPD_UPDATE_NEW)
select modify_user,modify_date,* from pes_stg_bol (nolock) 
WHERE BOL_ID IN (SELECT BOL_ID FROM TEMP_PES.DBO.DLPD_UPDATE_NEW)
