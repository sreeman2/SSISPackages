-- COUNT = 2789 ROWS
SELECT top 10 nvo_scac,* 
--INTO TEMP_PES.DBO.DLPD_UPDATE_NEW 
FROM PES.DBO.PES_STG_BOL STG(NOLOCK)
WHERE ISNULL(IS_DELETED,'') <> 'Y'
AND STG.RECORD_STATUS IN('CLEANSED','AUTOMATED') 
AND STG.BOL_STATUS IN('LATEMASTER', 'READY FOR RELEASE', 'READY')
and isnull(nvo_scac,'') <> ''

begin tran
